namespace Normal.GorillaTemplate.Keyboard {
    /// <summary>
    /// The type of keyboard button.
    /// </summary>
    public enum KeyboardButtonType {
        /// <summary>
        /// A regular button.
        /// </summary>
        Symbol,

        /// <summary>
        /// A button to delete an entry.
        /// </summary>
        Backspace,

        /// <summary>
        /// A button to submit an entry.
        /// </summary>
        Enter,

        /// <summary>
        /// Arrow navigation up.
        /// </summary>
        ArrowUp,

        /// <summary>
        /// Arrow navigation down.
        /// </summary>
        ArrowDown,

        /// <summary>
        /// Arrow navigation right.
        /// </summary>
        ArrowRight,

        /// <summary>
        /// Arrow navigation left.
        /// </summary>
        ArrowLeft,
    }
}
